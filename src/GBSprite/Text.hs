-- | Bitmap font rendering with a built-in 8x8 pixel font.
--
-- The built-in font covers printable ASCII (32-126). Characters
-- outside this range render as filled rectangles.
module GBSprite.Text
  ( -- * Types
    Font (..),

    -- * Built-in font
    defaultFont,

    -- * Rendering
    renderChar,
    renderText,
    textWidth,
    textHeight,
  )
where

import Data.Bits (testBit)
import Data.Word (Word8)
import GBSprite.Canvas (Canvas, newCanvas, setPixel)
import GBSprite.Color (Color, transparent)
import GBSprite.Compose (stamp)

-- | A bitmap font defined by character width, height, and glyph data.
data Font = Font
  { -- | Width of each character cell
    fontWidth :: !Int,
    -- | Height of each character cell
    fontHeight :: !Int,
    -- | Glyph data: function from character to bit rows
    fontGlyphs :: Char -> [Word8]
  }

instance Show Font where
  show f = "Font " ++ show (fontWidth f) ++ "x" ++ show (fontHeight f)

instance Eq Font where
  a == b = fontWidth a == fontWidth b && fontHeight a == fontHeight b

-- | The built-in 8x8 pixel font covering printable ASCII.
defaultFont :: Font
defaultFont =
  Font
    { fontWidth = defaultFontWidth,
      fontHeight = defaultFontHeight,
      fontGlyphs = lookupGlyph
    }

-- | Render a single character as a canvas.
renderChar :: Font -> Color -> Char -> Canvas
renderChar font color ch =
  let w = fontWidth font
      h = fontHeight font
      rows = fontGlyphs font ch
      blank = newCanvas w h transparent
   in drawGlyph blank w color rows 0

-- | Render a string as a canvas (horizontal layout, no wrapping).
renderText :: Font -> Color -> String -> Canvas
renderText font color text =
  let charW = fontWidth font
      charH = fontHeight font
      totalW = length text * charW
      blank = newCanvas totalW charH transparent
   in foldl (\c (i, ch) -> stamp c (i * charW) 0 (renderChar font color ch)) blank (zip [0 ..] text)

-- | Compute the width of rendered text in pixels.
textWidth :: Font -> String -> Int
textWidth font text = length text * fontWidth font

-- | The height of rendered text (always one line).
textHeight :: Font -> Int
textHeight = fontHeight

-- ---------------------------------------------------------------------------
-- Internal: glyph rendering
-- ---------------------------------------------------------------------------

drawGlyph :: Canvas -> Int -> Color -> [Word8] -> Int -> Canvas
drawGlyph canvas _ _ [] _ = canvas
drawGlyph canvas w color (row : rows) y =
  let drawn = drawRow canvas w color row y 0
   in drawGlyph drawn w color rows (y + 1)

drawRow :: Canvas -> Int -> Color -> Word8 -> Int -> Int -> Canvas
drawRow canvas w _ _ _ x | x >= w = canvas
drawRow canvas w color row y x =
  let bitIdx = w - 1 - x
      drawn =
        if testBit row bitIdx
          then setPixel canvas x y color
          else canvas
   in drawRow drawn w color row y (x + 1)

-- ---------------------------------------------------------------------------
-- Built-in font constants
-- ---------------------------------------------------------------------------

defaultFontWidth :: Int
defaultFontWidth = 8

defaultFontHeight :: Int
defaultFontHeight = 8

-- | Look up glyph data for a character.
-- Returns 8 bytes (one per row), MSB = leftmost pixel.
lookupGlyph :: Char -> [Word8]
lookupGlyph ch
  | ch >= ' ' && ch <= '~' =
      let idx = fromEnum ch - fromEnum ' '
       in if idx < length glyphData
            then glyphData !! idx
            else unknownGlyph
  | otherwise = unknownGlyph

-- | Filled rectangle for unknown characters.
unknownGlyph :: [Word8]
unknownGlyph = [0xFF, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0xFF]

